    void login(){
        try {
            User user = database.retrieveUser("user" , USERNAME.getText());
            if(user.getPassword().equals(PASSWORD.getText())){
                if(user.isActive() && user.getType().equals("admin")){
                    new adminMainpage(USERNAME.getText()).setVisible(true);
                }
                else if(user.isActive() && user.getType().equals("student")){
                    new studentMainpage(USERNAME.getText()).setVisible(true);
                }
                else if(user.isActive() && user.getType().equals("instructor")){
                    new instructorMainpage(USERNAME.getText()).setVisible(true);
                }
                else{
                    new waitForActivation(USERNAME.getText()).setVisible(true);
                }
                dispose();
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }
    
